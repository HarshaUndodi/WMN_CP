# ---------------------------------------------------------------------------
# Helper: add router_id and timestamp to each route entry
# ---------------------------------------------------------------------------
def _enrich_routes(routes: list[dict], router_id: str) -> list[dict]:
    """Add router_id and timestamp to each route dict."""
    ts = datetime.now().isoformat()
    enriched = []
    for r in routes:
        entry = dict(r)
        entry["router_id"] = router_id
        entry["timestamp"] = ts
        # Normalise empty gateway
        if not entry.get("gateway"):
            entry["gateway"] = "directly connected"
        enriched.append(entry)
    return enriched


# ---------------------------------------------------------------------------
# Source 1: Read from routing_tables.json (Mininet-generated)
# ---------------------------------------------------------------------------
def _read_routing_json() -> dict[str, list[dict]] | None:
    """Read routing_tables.json if it exists and is fresh (< FRESHNESS_THRESHOLD)."""
    if not os.path.exists(ROUTING_TABLES_JSON):
        return None

    age = time.time() - os.path.getmtime(ROUTING_TABLES_JSON)
    if age > FRESHNESS_THRESHOLD:
        return None

    try:
        with open(ROUTING_TABLES_JSON, "r") as f:
            data = json.load(f)
        # data is { "r1": [...], "r2": [...], ... }
        if isinstance(data, dict) and data:
            return data
    except (json.JSONDecodeError, OSError):
        pass
    return None


def _read_routing_json_any_age() -> dict[str, list[dict]] | None:
    """Read routing_tables.json regardless of age."""
    if not os.path.exists(ROUTING_TABLES_JSON):
        return None
    try:
        with open(ROUTING_TABLES_JSON, "r") as f:
            data = json.load(f)
        if isinstance(data, dict) and data:
            return data
    except (json.JSONDecodeError, OSError):
        pass
    return None


# ---------------------------------------------------------------------------
# Source 2: ip route show (system)
# ---------------------------------------------------------------------------
def _parse_ip_route_line(line: str) -> dict | None:
    """Parse a single line of 'ip route show' output."""
    parts = line.strip().split()
    if not parts:
        return None

    route: dict = {
        "destination": parts[0],
        "gateway": "directly connected",
        "interface": "unknown",
        "metric": 0,
    }

    i = 1
    while i < len(parts):
        token = parts[i]
        if token == "via" and i + 1 < len(parts):
            route["gateway"] = parts[i + 1]
            i += 2
        elif token == "dev" and i + 1 < len(parts):
            route["interface"] = parts[i + 1]
            i += 2
        elif token == "metric" and i + 1 < len(parts):
            try:
                route["metric"] = int(parts[i + 1])
            except ValueError:
                route["metric"] = 0
            i += 2
        else:
            i += 1
    return route


def _poll_system_routes() -> list[dict] | None:
    """Run 'ip route show' and parse the output."""
    try:
        result = subprocess.run(
            ["ip", "route", "show"],
            capture_output=True, text=True, timeout=5,
        )
        if result.returncode != 0:
            return None
        routes = []
        for line in result.stdout.strip().splitlines():
            parsed = _parse_ip_route_line(line)
            if parsed:
                routes.append(parsed)
        return routes if routes else None
    except Exception:
        return None


