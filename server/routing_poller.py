def poll_all_routers() -> dict[str, list[dict]]:
    """Return routing tables for ALL routers (multi-router).

    Priority: routing_tables.json (fresh) → routing_tables.json (any age) → mock.
    Returns: { "r1": [ {route}, ... ], "r2": [...], ... }
    """
    # 1. Try fresh JSON from Mininet
    data = _read_routing_json()
    if data:
        source = "routing_tables.json (live)"
    else:
        # 2. Try any-age JSON
        data = _read_routing_json_any_age()
        if data:
            source = "routing_tables.json (cached)"
        else:
            # 3. Mock fallback
            data = {k: [dict(r) for r in v] for k, v in MOCK_ROUTES.items()}
            source = "mock data"

    # Enrich with router_id and timestamp
    result = {}
    for router_id, routes in data.items():
        result[router_id] = _enrich_routes(routes, router_id)

    return result


def poll_routing_table() -> list[dict]:
    """Return a flat list of ALL routes across ALL routers.

    This maintains backward compatibility with the original API.
    """
    all_routers = poll_all_routers()
    flat = []
    for router_id, routes in sorted(all_routers.items()):
        flat.extend(routes)
    return flat


def poll_single_router(router_id: str) -> list[dict]:
    """Return routing table for a specific router."""
    all_routers = poll_all_routers()
    if router_id in all_routers:
        return all_routers[router_id]
    return []


def get_topology() -> dict:
    """Return the network topology definition."""
    return dict(TOPOLOGY)


def get_data_source_info() -> dict:
    """Return info about the current data source."""
    json_exists = os.path.exists(ROUTING_TABLES_JSON)
    json_age = None
    if json_exists:
        json_age = round(time.time() - os.path.getmtime(ROUTING_TABLES_JSON), 1)

    return {
        "routing_tables_json": ROUTING_TABLES_JSON,
        "json_exists": json_exists,
        "json_age_seconds": json_age,
        "json_fresh": json_exists and json_age is not None and json_age < FRESHNESS_THRESHOLD,
        "freshness_threshold": FRESHNESS_THRESHOLD,
    }


# ---------------------------------------------------------------------------
# File watcher: detect changes to routing_tables.json
# ---------------------------------------------------------------------------
class RoutingFileWatcher:
    """Polls routing_tables.json for changes and triggers a callback."""

    def __init__(self, callback=None, interval: float = 5.0):
        self._callback = callback
        self._interval = interval
        self._last_mtime: float = 0.0
        self._running = False
        self._thread: threading.Thread | None = None

    def start(self):
        """Start watching in a background thread."""
        self._running = True
        self._thread = threading.Thread(target=self._watch_loop, daemon=True)
        self._thread.start()

    def stop(self):
        """Stop the watcher."""
        self._running = False
        if self._thread:
            self._thread.join(timeout=self._interval + 1)

    def _watch_loop(self):
        while self._running:
            try:
                if os.path.exists(ROUTING_TABLES_JSON):
                    mtime = os.path.getmtime(ROUTING_TABLES_JSON)
                    if mtime != self._last_mtime and self._last_mtime > 0:
                        # File changed!
                        if self._callback:
                            self._callback(ROUTING_TABLES_JSON, mtime)
                    self._last_mtime = mtime
            except OSError:
                pass
            time.sleep(self._interval)


def watch_routing_file(callback=None, interval: float = 5.0) -> RoutingFileWatcher:
    """Start watching routing_tables.json for changes.

    Args:
        callback: function(filepath, mtime) called when file changes.
        interval: poll interval in seconds.

    Returns:
        RoutingFileWatcher instance (call .stop() to end).
    """
    watcher = RoutingFileWatcher(callback=callback, interval=interval)
    watcher.start()
    return watcher


